int	ft_str_is_numeric(char *str)
{
	int	i;

	i = 0;
	if (!str[i])
	{
		return (1);
	}
	while (*str)
	{
		if (!(*str >= 48 && *str <= 57))
		{
			return (0);
		}
		str++;
	}
	return (1);
}
